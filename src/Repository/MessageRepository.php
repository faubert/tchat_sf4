                        INNER JOIN connexion c ON m.connexion_id = c.id
                        INNER JOIN user u ON c.user_id = u.id
                        ORDER BY m.date_create ASC";

        $stmt = $this->getEntityManager()->getConnection()->prepare($rawSql);

        $stmt->execute();

        return $stmt->fetchAllAssociative();
